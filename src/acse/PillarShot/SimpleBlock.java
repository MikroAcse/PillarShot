package acse.PillarShot;

public class SimpleBlock {
    public int x;
    public int y;
    public int z;
    public String type;
    public String world;

    public SimpleBlock(int x, int y, int z, String type, String world) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.type = type;
        this.world = world;
    }
}
